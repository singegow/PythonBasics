# Tuples are similar to list, but they are immutable
# we cant modify, remove or existing items
# we cant mute or change it
# [] brackets for list and () for tuples
# There are only two methods in tuples.
# one is count and one more in index
numbers = (1, 2, 3)
print(numbers.count(1))
print(numbers.index(2))
print(numbers[0])
